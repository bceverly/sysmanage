def insert_dotted(target: dict, dotted_key: str, value):
    """Insert ``value`` into ``target`` at the dotted path ``dotted_key``.

    Creates missing intermediate dicts.  Leaves existing leaf keys in
    place — idempotent so the script is safe to re-run.
    """
    parts = dotted_key.split(".")
    for part in parts[:-1]:
        node = target.setdefault(part, {})
        if not isinstance(node, dict):
            raise SystemExit(
                f"Conflict: {dotted_key} would clobber non-dict at {part!r}"
            )
        target = node
    last = parts[-1]
    if last in target and target[last]:
        return  # idempotent — leave existing value
    target[last] = value


def main():
    expected = sorted(TRANSLATIONS["en"].keys())
    for locale, mapping in TRANSLATIONS.items():
        missing = set(expected) - set(mapping.keys())
        if missing:
            raise SystemExit(
                f"Locale {locale!r} missing keys vs en: {sorted(missing)}"
            )
    for locale, mapping in TRANSLATIONS.items():
        path = LOCALES_DIR / locale / "translation.json"
        if not path.exists():
            print(f"SKIP: {path} does not exist")
            continue
        data = json.loads(path.read_text(encoding="utf-8"))
        for key, value in mapping.items():
            insert_dotted(data, key, value)
        path.write_text(
            json.dumps(data, indent=2, ensure_ascii=False) + "\n", encoding="utf-8"
        )
        print(f"OK:   {path} ({len(mapping)} keys merged)")


if __name__ == "__main__":
    main()
